package day050perators;

public class Decrement01 {

	public static void main(String[] args) {
		// int data type inde bir variable olusturun .bu variableyi uc yontemide kullanarak bir azaltin
		
		int num1 = 10;
		num1 = num1 - 1;
		num1-=1;
		num1--;

		System.out.println(num1);
		// int data type inde bir variable olusturun .bu variable degerini carpma ve bolme kullanarak artirip azaltalim
		//  carpam ile increment
		int num2 = 10 ;
		num2 = num2 * 3;
		num2*=3;
		System.out.println(num2);
		
		num2 = num2 / 10;
		num2/=9;
		System.out.println(num2);
		
		
		
		
		
		
		
		
		
		
		
	}

}
